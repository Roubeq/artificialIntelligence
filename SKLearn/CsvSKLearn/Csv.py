def create_dataset(output_file, samples_per_intent=35):
    """Создание датасета с указанным количеством примеров на интент"""

    dataset = []

    for intent in INTENTS:
        examples = EXAMPLES[intent]

        # Берем нужное количество примеров (от 30 до 40)
        if samples_per_intent <= len(examples):
            samples = random.sample(examples, samples_per_intent)
        else:
            # Если примеров меньше, дублируем с вариациями
            samples = examples.copy()
            while len(samples) < samples_per_intent:
                # Добавляем вариации существующих примеров
                for ex in examples:
                    if len(samples) >= samples_per_intent:
                        break
                    # Добавляем с восклицательным знаком
                    if ex + "!" not in samples:
                        samples.append(ex + "!")
                    if len(samples) >= samples_per_intent:
                        break
                    # Добавляем с вопросом
                    if ex + "?" not in samples:
                        samples.append(ex + "?")

        # Добавляем в датасет
        for text in samples[:samples_per_intent]:
            dataset.append({'text': text, 'intent': intent})

    # Перемешиваем
    random.shuffle(dataset)

    # Сохраняем в CSV
    with open(output_file, 'w', encoding='utf-8', newline='') as f:
        writer = csv.DictWriter(f, fieldnames=['text', 'intent'])
        writer.writeheader()
        writer.writerows(dataset)

    # Статистика
    from collections import Counter
    intents_count = Counter([item['intent'] for item in dataset])

    print(f"✅ Создан датасет: {output_file}")
    print(f"📊 Всего примеров: {len(dataset)}")
    print("\n📈 Распределение по интентам:")
    total = len(dataset)
    for intent in INTENTS:
        count = intents_count.get(intent, 0)
        percentage = (count / total) * 100
        print(f"  {intent:20s}: {count:3d} примеров ({percentage:.1f}%)")

    return dataset


# Создаем датасет с 35 примерами на интент
create_dataset('intent_dataset_final.csv', samples_per_intent=35)